# 리스트 생성 문법
#리스트 식별자 = [요소값 1, 요소값 2, .... 요소값n]
# 리스트 크기는 가변적
# 다양한 종류의 데이터를 하나의 리스트에 저장 가능

scores = 100 # 변수
print(scores)

scores = [100] # 리스트
print(scores)

scores = [100, 200, 300,'인형']
print(scores)

print(scores[1])

